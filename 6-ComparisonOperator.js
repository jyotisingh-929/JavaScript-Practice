// Comparison Operators

let a = 5;
let b = 4;
console.log("a = ",a, ", b = ",b);

// equal to (==)
console.log("5 == 4 is",a == b);

// not equal to (!=)
console.log("5 != 4 is",a != b);

// equal to & data type (===)
console.log("5 === 4 is",a === b);

// not equal to & data type (===)
console.log("5 !== 4 is",a !== b);

// greater than equal to (>=)
console.log("5 >= 4 is",a >= b);

// less than equal to (<=)
console.log("5 <= 4 is",a <= b);
