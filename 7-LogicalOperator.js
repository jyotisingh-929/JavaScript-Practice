// Logical Operators

let a = 5;
let b = 4;
console.log("a =",a,", b =",b);

// AND ( && )
console.log("cond1 && cond2 =",a < b && a == b);
console.log("cond1 && cond2 =",a > b && a != b);

// OR ( || )
console.log("cond1 || cond2 =",a < b || a == b);
console.log("cond1 || cond2 =",a > b || a == b);

// NOT ( ! )
console.log("!(5 > 4) =",!(a > b));  // true ko false and false ko true return krta hai.
console.log("!(5 === 4) =",!(a === b));



