// Arithmetic Operators

let a = 5;
let b = 4;
console.log("a = ",a, ", b = ",b);
// plus or addition
console.log("a + b = ",a + b);

// minus or subtraction
console.log("a - b = ",a - b);

// multiply
console.log("a * b = ",a * b);

// division
console.log("a / b = ",a / b);

// modulus
console.log("a % b = ",a % b);

// exponentiation or a to the power of 2 (a^b)
console.log("a ** b = ",a ** b);    // a ^ b = 625 



